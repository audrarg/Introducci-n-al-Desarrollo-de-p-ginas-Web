function cambioIMG() {

    if (document.getElementById("imgClickAndChange").src == "conversorDivisasimg/star.png") 
    {
        document.getElementById("imgClickAndChange").src = "conversorDivisasimg/starY.png";
    }
    else 
    {
        document.getElementById("imgClickAndChange").src = "conversorDivisasimg/star.png";
    }
}